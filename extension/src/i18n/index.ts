export const dictionaries = {
  en,
  'zh-CN': zhCN
} as const;

export type TranslationKey = keyof typeof en;
export type Translator = (key: TranslationKey, params?: TranslationParams) => string;

export function normalizeLocale(value: string | null | undefined): Locale {
  const normalized = value?.trim().toLowerCase();
  if (normalized?.startsWith('zh')) return 'zh-CN';
  return 'en';
}

export function effectiveLocale(
  preference: LocalePreference | null | undefined,
  navigatorLike: Pick<Navigator, 'language' | 'languages'> = navigator
): Locale {
  if (preference === 'en' || preference === 'zh-CN') return preference;
  return normalizeLocale(
    navigatorLike.languages?.[0] ?? navigatorLike.language ?? 'en'
  );
}

export function translate(
  locale: Locale,
  key: TranslationKey,
  params: TranslationParams = {}
): string {
  const template = dictionaries[locale][key] ?? dictionaries.en[key];
  return template.replace(/\{(\w+)\}/g, (match, name: string) => {
    const value = params[name];
    return value === undefined ? match : String(value);
  });
}

export function createTranslator(locale: Locale): Translator {
  return (key, params) => translate(locale, key, params);
}

export const englishTranslator = createTranslator('en');
